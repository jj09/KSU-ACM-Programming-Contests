import java.io.*;

public class TriangleGame {

	/**
	 * @param args
	 * @throws IOException
	 */
	public static void main(String[] args) throws IOException {
		BufferedReader stdin = new BufferedReader(new InputStreamReader(
				System.in));
		String input = "";

		while (input.compareTo("$") != 0) {
			input = stdin.readLine();
		}

	}

}
